def build_csv():
    years = list(range(START, END + 1))
    rows = []
    for city, cps in CHECKPOINTS.items():
        s = pd.Series(cps, dtype=float).reindex(range(START, END + 1))
        first = min(cps)
        s.loc[:first] = s.loc[:first].fillna(0.0)  # city not (notably) there yet
        s = s.interpolate(method='linear')
        for y in years:
            rows.append((city, y, round(s[y] / 1000.0, 3)))  # -> millions
    df = pd.DataFrame(rows, columns=["City", "Year", "Population"])
    out = Path("cache/cities_clean.csv")
    df.to_csv(out, index=False)
    print(f"wrote {out} — {df.City.nunique()} cities × {len(years)} years")


def build_flag_tiles():
    dst = Path("cache/artist_photos")
    dst.mkdir(parents=True, exist_ok=True)
    for city, country in FLAG_OF.items():
        src = Path("cache/flags") / f"{country}.png"
        if not src.exists():
            print(f"  MISSING flag: {country}")
            continue
        im = Image.open(src).convert("RGBA")
        w, h = im.size
        side = min(w, h)
        im = im.crop(((w - side) // 2, (h - side) // 2,
                      (w - side) // 2 + side, (h - side) // 2 + side))
        im.save(dst / f"{city}.png")
    print(f"flag tiles -> {dst}")


if __name__ == "__main__":
    build_csv()
    build_flag_tiles()
